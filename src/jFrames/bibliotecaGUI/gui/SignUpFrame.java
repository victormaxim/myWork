    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user = jTextField1.getText();
        String p1 = new String(jPasswordField1.getPassword());
        String p2 = new String(jPasswordField2.getPassword());
        if (LoginContainer.getInstance().getUser(user) == null) {
            if (p1.equals(p2)) {
                LoginContainer.getInstance().addUser(new User(user, p2));
                try {
                    out = new ObjectOutputStream(new FileOutputStream("useri.dat"));
                    out.writeObject(LoginContainer.getInstance());
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Eroare la scriere", "eroare", JOptionPane.ERROR_MESSAGE);
                }
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Parolele nu corespund", "eroare", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "User existent", "eroare", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed


